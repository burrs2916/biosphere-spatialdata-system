interface IconCategoryTabsProps<T> {
  categories: Array<{ label: string; items: T[] }>;
  onSelect: (catIndex: number, itemIndex: number) => void;
  renderItem: (item: T) => React.ReactNode;
  showItemName?: boolean;
}

function IconCategoryTabs<T>({ categories, onSelect, renderItem, showItemName }: IconCategoryTabsProps<T>) {
  const [tabIndex, setTabIndex] = React.useState(0);

  return (
    <Box>
      <Tabs
        value={tabIndex}
        onChange={(_, newValue) => setTabIndex(newValue)}
        variant="scrollable"
        scrollButtons="auto"
        sx={{ mb: 2 }}
      >
        {categories.map((cat, index) => (
          <Tab key={index} label={cat.label} />
        ))}
      </Tabs>
      <Box
        sx={{
          display: "grid",
          gridTemplateColumns: "repeat(8, 1fr)",
          gap: 1.5,
        }}
      >
        {categories[tabIndex]?.items.map((item, _index) => (
          <Paper
            key={_index}
            sx={{
              p: 1.5,
              display: "flex",
              flexDirection: "column",
              alignItems: "center",
              gap: 0.5,
              borderRadius: 2,
              cursor: "pointer",
              border: "1px solid",
              borderColor: "divider",
              "&:hover": {
                bgcolor: "action.hover",
                borderColor: "primary.main",
                transform: "scale(1.05)",
                transition: "transform 0.1s",
              },
            }}
            onClick={() => onSelect(tabIndex, _index)}
          >
            {item && renderItem(item)}
            {showItemName && typeof item === "object" && item && "name" in item && (
              <Typography variant="caption" sx={{ fontSize: "0.65rem" }}>
                {(item as any).name}
              </Typography>
            )}
          </Paper>
        ))}
      </Box>
    </Box>
  );
}

